ll power(ll x, ll y, ll p) {
    // (x^y) mod p
    ll res = 1;
    x %= p;
    while (y) {
        if (y & 1)
            res = (res * x) % p;
        y >>= 1;
        x = (x * x) % p;
    }
    return res;
}

